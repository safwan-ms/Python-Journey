# for loop
my_list = [1, 2, 3, 4, 5]

for element in my_list:
    print(element)
# ------------------------

# ------------------------
#  Range & Length
my_list = [1, 2, 3, 4, 5]

for i in range(len(my_list)):
    print(my_list[i])

# While loop
my_list = [1, 2, 3, 4, 5]
i = 0
while i < len(my_list):
    print(my_list[i])
    i += 1
